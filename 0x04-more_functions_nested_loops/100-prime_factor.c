#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
**main - Entry point
*
* Description: Write a program that finds and prints the largest prime factor
* of the number 612852475143, followed by a new line.
*
* Return: Always 0 (success)
*/
int main(void)
{
int n = 612852475143;
int i;
if (n % i != 0)
{
for (i = 2 ; i <= n; i++)
{
printf("%i/n", i);
}
}
}
